            $required_master_job_keys = null;

            $area = Area::updateOrCreate(
                ['id' => (int)$dungeonIdStr], // IDで一意に更新（名前が変わった場合にも対応）
                [
                    'name' => $name,
                    'slug' => $slug,
                    'description' => $description,
                    'recommended_level_min' => $levelMin,
                    'recommended_level_max' => $levelMax,
